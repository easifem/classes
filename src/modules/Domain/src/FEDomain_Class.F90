  PROCEDURE, PUBLIC, PASS(obj) :: DEALLOCATE => obj_Deallocate
  !! Deallocate data stored inside an instance of domain
  !! TODO Rename Deallocate to Deallocate
  FINAL :: obj_Final
  !! Finalizer for domain

  ! IO:
